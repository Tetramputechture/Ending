package ending.tile;

/**
 * The different types of Tiles.
 * @author Nick
 */
public enum TileType {

    /**
     * The Unused Tile.
     */
    UNUSED,

    /**
     * The Dirt Wall Tile.
     */
    DIRTWALL,

    /**
     * The Dirt Floor Tile.
     */
    DIRTFLOOR,

    /**
     * The Stone Wall Tile.
     */
    STONEWALL,

    /**
     * The Stone Floor Tile.
     */
    STONEFLOOR,

    /**
     * The Stone Corridor Tile.
     */
    STONECORRIDOR,

    /**
     * The Door Tile.
     */
    DOOR,

    /**
     * The Up Stairs Tile.
     */
    UPSTAIRS,

    /**
     * The Down Stairs Tile.
     */
    DOWNSTAIRS

}
